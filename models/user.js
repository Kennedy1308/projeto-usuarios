class User { // variavel em classe vira um atributo ou propriedade desse objeto
                // classe = conjunto de atributos e metodos
                    // atributos = variaveis
                        //metodos = acoes, comandos
    constructor(name, gender, birth, country, email, password, photo, admin) { // metodo logo apos vem os argumentos

        this.name = name; // "this.name" e' o atributo    -   o "name" e' a variavel acima
        this.gender = gender; // this serve para referir a atributos e metodos da classe
        this.birth = birth;
        this.country = country;
        this.email = email;
        this.password = password;
        this.photo = photo;
        this.admin = admin;




    }


}

